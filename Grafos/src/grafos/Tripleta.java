package grafos;

/**
 *
 * @author LENOVO
 */
public class Tripleta {
    private int f;
    private int c;
    private int v;

    public Tripleta(int f, int c, int v) {
        this.f = f;
        this.c = c;
        this.v = v;
    }

    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }

    public int getF() {
        return f;
    }

    public void setF(int f) {
        this.f = f;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
    
    
}
